// Array para almacenar los marcadores
let markers = [];

// Función para mostrar u ocultar marcadores según el checkbox seleccionado
function updateMarkers() {
  for (let i = 0; i < markers.length; i++) {
    let sector = markers[i].sector;

    if (
      (sector === "EntornoCantabrico" &&
        document.getElementById("EntornoCantabrico").checked) ||
      (sector === "FactoriaVegetal" &&
        document.getElementById("FactoriaVegetal").checked) ||
      (sector === "JardinIsla" &&
        document.getElementById("JardinIsla").checked) ||
      (sector === "ItinerarioAtlantico" &&
        document.getElementById("ItinerarioAtlantico").checked)
    ) {
      map.addLayer(markers[i].marker); // Mostrar marcador
    } else {
      map.removeLayer(markers[i].marker); // Ocultar marcador
    }
  }
}

// Asignación de sector a cada rango de índices
for (let i = 0; i < coleBot.length; i++) {
  let iconUrl = "";
  let sector = ""; // Almacena el sector de cada marcador

  // Definir la URL del icono en función del índice y asignar sector
  if (i < 12) {
    iconUrl = "images/markCant.png";
    sector = "EntornoCantabrico";
  } else if (i < 19) {
    iconUrl = "images/markVeg.png";
    sector = "FactoriaVegetal";
  } else if (i < 28) {
    iconUrl = "images/markIsla.png";
    sector = "JardinIsla";
  } else {
    iconUrl = "images/markAtl.png";
    sector = "ItinerarioAtlantico";
  }

  // Crear el marcador con su número y icono
  var markColeBot = new L.Marker(new L.LatLng(coleBot[i][0], coleBot[i][1]), {
    icon: new L.NumberedDivIcon({
      number: markerNumbers[i], // Usar el número correspondiente
      iconUrl: iconUrl, // Definir el icono según el índice
    }),
  });

  // Almacenar el marcador y su sector en el array
  markers.push({ marker: markColeBot, sector: sector });

  // Asociar un popup con la información correspondiente
  markColeBot.bindPopup(
    "<strong>" + coleBot[i][2] + "</strong><br><i>" + coleBot[i][3] + "</i>"
  );
}

// Inicialmente mostrar los marcadores
updateMarkers();

